#Создайте класс "Фигура", который содержит метод расчета площади фигуры.
#Создайте классы "Квадрат" и "Прямоугольник", которые наследуются от класса
#"Фигура". Каждый класс должен иметь метод расчета площади собственной фигуры.

import math

class Фигура:
    def __init__(self):
        pass
    def рассчитать_площадь(self):
        return 0

class Квадрат(Фигура):
    def __init__(self, сторона):
        super().__init__()
        self.сторона = сторона
    def рассчитать_площадь(self):
        return self.сторона ** 2

class Прямоугольник(Фигура):
    def __init__(self, длина, ширина):
        super().__init__()
        self.длина = длина
        self.ширина = ширина
    def рассчитать_площадь(self):
        return self.длина * self.ширина

if __name__ == "__main__":
    квадрат = Квадрат(5)
    прямоугольник = Прямоугольник(4, 6)
    фигура = Фигура()

    print(f"Площадь квадрата: {квадрат.рассчитать_площадь()}")
    print(f"Площадь прямоугольника: {прямоугольник.рассчитать_площадь()}")
    print(f"Площадь фигуры (базовый класс): {фигура.рассчитать_площадь()}")
